    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        try {
             if(
                nameTxt.getText().trim().isEmpty()
                ||quantityTxt.getText().trim().isEmpty()
                 ){
            JOptionPane.showMessageDialog(this, "fill this filled");
             }else{
            Registry theRegistry = LocateRegistry.getRegistry("127.0.0.1",5001);
            StockService service =  (StockService) theRegistry.lookup("stocks");
            Stock theStock = new Stock();
            theStock.setName(nameTxt.getText());
            theStock.setQuantity(Integer.parseInt(quantityTxt.getText()));
            Stock theObj = service.addStock(theStock);
            if(theObj!=null){
                JOptionPane.showMessageDialog(this, "Data saved");
            }else{
                JOptionPane.showMessageDialog(this, "Data not saved");
            }}
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_addBtnActionPerformed

