    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
    try {
            Registry theRegistry = LocateRegistry.getRegistry("127.0.0.1",5001);
            LoginsService service =  (LoginsService) theRegistry.lookup("log");
            Login theLogin = new Login();
            
            theLogin.setEmail(emailTxt.getText());
            theLogin.setRole((String) roleCombo.getSelectedItem());
            theLogin.setPassword(passwordTxt.getText());
            String rol ;
            Login theObj = service.adminLogin(theLogin.getEmail(), theLogin.getPassword(),theLogin.getRole());
            if(theObj!=null ){
                rol = theObj.getRole();
                if(roleCombo.getSelectedItem().equals("Site manager")){
                SiteForm site = new SiteForm();
                site.show();
                this.hide();
            }else if(roleCombo.getSelectedItem().equals("Admin"))
                    {
                AccountForm acc = new AccountForm();
                acc.show();
                this.hide();
            }else{
                JOptionPane.showMessageDialog(this, "password not match");
            } }else{
               JOptionPane.showMessageDialog(this, "password not match"); 
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_addBtnActionPerformed

